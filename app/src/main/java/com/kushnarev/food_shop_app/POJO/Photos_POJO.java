package com.kushnarev.food_shop_app.POJO;

public class Photos_POJO {
    private String title;
    private String body;
    private String url;

    public Photos_POJO(String title, String body) {
        this.title = title;
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

    public String getUrl() {
        return url;
    }



}
